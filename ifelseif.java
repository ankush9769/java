public class ifelseif {
    public static void main(String[] args) {
        int marks = 80;
        if (marks > 90)
        {
            System.out.println("the grade is A");
        }
        else if ((marks <= 90) && (marks > 80))
        {
            System.out.println("the grade is B");
        }
        else {
            System.out.println("the grade is C");
        }
    }
}
