public class Literals {
    public static void main(String[] args) {
        //the constant value that can be store in the variable is called literals
        byte age = 18;      //int literal
        char div = 'A';    //char literal
        float num = 1.3f;  //float literal with "f"
        double pi = 3.14;  //double literal
        long phoneno = 1234567890L;  //long literal
        boolean value = true;        //boolean literal
        String str = "ankush";       //string literal
        System.out.println(str);
    }
}
